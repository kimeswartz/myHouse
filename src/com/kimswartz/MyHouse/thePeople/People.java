package com.kimswartz.MyHouse.thePeople;

public class People {

    private String name;
    private String hair;
    private int age;

    public People(String name, String hair, int age) {
        this.name = name;
        this.hair = hair;
        this.age = age;

    }

    public String getName() {
        return name;
    }

    public String getHair() {
        return hair;
    }

    public int getAge() {
        return age;
    }

}


